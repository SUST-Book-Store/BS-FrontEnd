<template>
<div  class="login-container"  >
    <el-form ref="form" :model="form"   :rules="rules" class="login-page">
      <h2 class="title" style="margin-bottom: 20px">用户登陆</h2>
      <el-form-item prop="phone" >
        <el-input v-model="form.phone"  clearable>
          <template #prefix>
            <el-icon class="el-input__icon">😃</el-icon>
          </template>
        </el-input>
      </el-form-item>
      <el-form-item prop="password">
        <el-input v-model="form.password"  clearable show-password>
          <template #prefix>
            <el-icon class="el-input__icon">🔒</el-icon>
          </template>
        </el-input>
      </el-form-item>
      <el-form-item >
        <el-button type="primary"  style=" width: 100%" @click="login">登 录</el-button>
      </el-form-item>
      <el-form-item><el-button type="text" @click="$router.push('/user/register/')">前往注册 >> </el-button></el-form-item>
    </el-form>
</div>

</template>

<script>
import request from "../utils/request";
import {ElMessage} from "element-plus";

export default {
  name: "Login",
  data() {
    return {
      form: {},
      rules: {
        phone: [
          {
            required: true,
            message: '请输入手机号',
            trigger: 'blur',
          },
            {
            pattern: /^\d+$/,
            message: '手机号必须为纯数字',
            trigger: 'blur',
            }
        ],
        password: [
          {
            required: true,
            message: '请输入密码',
            trigger: 'blur',
          }
        ]

      }

    }
  },
  methods: {
    login(){
      this.$refs['form'].validate((valid) => {
        if (valid) {
          request.post("http://127.0.0.1:3000/user/login", this.form).then(res => {
            if (res.data.status == 0) {
              ElMessage.success("登录成功")
              sessionStorage.setItem("token", res.data.accessToken)//缓存用户信息
            //   this.$_setStorage({Authorization: res.data.accessToken});
              this.$router.push("/")
            } else {
              ElMessage.error(res.data.msg)
            }
          })
        }
      })
    }
  }
}

</script>

